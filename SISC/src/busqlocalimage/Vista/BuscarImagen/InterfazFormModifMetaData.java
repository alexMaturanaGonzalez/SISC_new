    private void completarInfoImagen(int num) {

        icon = new ImageIcon(arrayImagenResult.get(num).getRuta());
        vistaImagen(icon);

        String nameImagen = (arrayImagenResult.get(num).getNombre()).replace(".jpg", "");

        this.jTextNombre.setText(nameImagen.trim());
        this.jTextTamano.setText(String.valueOf(arrayImagenResult.get(num).getTamano()));
        this.jTextFormato.setText(arrayImagenResult.get(num).getFormato());
        this.jTextFechaModif.setText(arrayImagenResult.get(num).getFechaModif());
        this.jTextDimensiones.setText(arrayImagenResult.get(num).getDimensiones());
        this.jTextDescrip.setText(arrayImagenResult.get(num).getDescrip());
    }//fin metodo completar info de imágenes

