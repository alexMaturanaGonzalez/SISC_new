    private void jNextMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jNextMouseClicked

        numTemp = numTemp + 1;
        if (numTemp < arrayImagenResult.size()) {

            completarInfoImagen(numTemp);
            jNext.setVisible(true);
        } else {

            //jNext.setVisible(false);
            numTemp = (arrayImagenResult.size()) - 1;
        }

    }//GEN-LAST:event_jNextMouseClicked

    private void jPrevMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jPrevMouseClicked

        numTemp = numTemp - 1;
        if (numTemp >= 0) {

            completarInfoImagen(numTemp);
            jPrev.setVisible(true);
        } else {

            //jPrev.setVisible(false);
            numTemp = 0;
        }


    }//GEN-LAST:event_jPrevMouseClicked

    private void jButtonModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonModificarActionPerformed

        rutaImagen = (arrayImagenResult.get(numTemp).getRuta());
        String nombreImagen = arrayImagenResult.get(numTemp).getNombre();

        String nameImagen = (arrayImagenResult.get(numTemp).getNombre()).replace(".jpg", "");

        String nameOrig = nameImagen.trim();
        String extOrig = (arrayImagenResult.get(numTemp).getFormato()).trim();
        extDescrip = (arrayImagenResult.get(numTemp).getDescrip()).trim();

        String newName = this.jTextNombre.getText().trim();
        String newExt = this.jTextFormato.getText().trim();
        String newDescrip = this.jTextDescrip.getText().trim();


        if (!nameOrig.equals(newName) || !extOrig.equals(newExt) || !extDescrip.equals(newDescrip)) {

            if (extOrig.equals(newExt)) {

                newExt = ".jpg";
            } else {

                newExt = "." + this.jTextFormato.getText();
            }

            if (!nameOrig.equals(newName) || !extOrig.equals(newExt)) {

                controladorImagen.renombrarImagen(rutaImagen, nombreImagen, newName, newExt);
                this.jLabelError.setText("Archivo Renombrado!");
                this.jLabelError.setVisible(true);

                arrayImagenResult.get(numTemp).setNombre(newName.concat(newExt));
                rutaImagen = rutaImagen.replace(nombreImagen, newName.concat(newExt));

                arrayImagenResult.get(numTemp).setRuta(rutaImagen);
                arrayImagenResult.get(numTemp).setFormato(this.jTextFormato.getText());

                jPanelBuscarImagenResultadosLista.mostrarDatos(arrayImagenResult);
                jPanelBuscarImagenResultadosVista.mostrarDatos(arrayImagenResult);

                this.dispose();
            }

            if (!extDescrip.equals(newDescrip)) {

                icon.setDescription("Holalalalalala");
            }
        }

    }//GEN-LAST:event_jButtonModificarActionPerformed

